#include "checker.h"

void	is_int(char **str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (ft_atol(str[i]) < -2147483648 ||
				ft_atol(str[i]) > 2147483647)
		{
			ft_putendl("Error");
			exit(0);
		}
		i++;
	}
}
